import { readFile, writeFile } from 'node:fs/promises'
import { resolve } from 'node:path'

const root = resolve(import.meta.dirname, '..')
const packageJson = JSON.parse(await readFile(resolve(root, 'package.json'), 'utf8'))
const exactDependency = (name) => {
  const value = packageJson.dependencies?.[name]
  if (typeof value !== 'string' || !/^\d+\.\d+\.\d+(?:[-+][0-9A-Za-z.-]+)?$/.test(value)) {
    throw new Error(`${name} must be an exact dependency before build metadata can be generated`)
  }
  return value
}

const quote = value => JSON.stringify(value)
const version = process.env.WORKFLOW_MCP_BUILD_VERSION ?? packageJson.version
const revision = process.env.WORKFLOW_MCP_BUILD_REVISION ?? 'development'
const source = `// Generated by scripts/generate-build-metadata.mjs. Do not hand-edit.\n` +
  `export const WORKFLOW_MCP_VERSION = ${quote(version)}\n` +
  `export const WORKFLOW_MCP_REVISION = ${quote(revision)}\n` +
  `export const CODEX_SDK_VERSION = ${quote(exactDependency('@openai/codex-sdk'))}\n` +
  `export const MCP_SDK_VERSION = ${quote(exactDependency('@modelcontextprotocol/sdk'))}\n` +
  `export const MCP_STABLE_PROTOCOL_VERSION = '2025-11-25'\n`

await writeFile(resolve(root, 'src', 'generatedBuildMetadata.ts'), source, 'utf8')
